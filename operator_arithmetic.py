# 사칙연산
print(2 * 3)
print(2 + 3)
print(2 - 3)
print(2 * 3)

print(2 / 3)
print(2 / 3.0)
print(2.0 / 3)

# 몫 연산자 (//),  **(지수승),  %(나머지연산)
print(2 // 3)
print(2 ** 3)
print(2 % 3)

# divmode() 함수
t = divmod(2, 3)
print(t, type(t))

a, b = divmod(2, 3)
print(a, b)

# 연산자 우선순위
print(2 + 3 *4)
print(-2 + 3 *4)
print(-(2 + 3) *4)

#결합순서
print(2 ** 3 ** 4)
print((2 ** 3) ** 4)
print(2 ** (3 ** 4))



